static volatile int DACValue;
int sample_no=0;

/* TIM2 and TIM6 elapse callback function. Invoked from TIM2_IRQHandler  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
 if (htim==&htim2)
 {
   DACValue=SIGNAL_MAGNITUDE*sample_no/SIGNAL_STEPS_IN_PERIOD; /* calculate new value */
  		
   HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, DACValue); /* output DAC */
   HAL_DAC_Start(&hdac, DAC_CHANNEL_1); /* start conversion */
	
  if (sample_no<SIGNAL_STEPS_IN_PERIOD) /* update DAC sample number */
     sample_no++;
   else
	   sample_no=0;
 }	 
}
